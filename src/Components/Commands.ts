import TimestampPlugin from "src/main";
import * as utils from "./utils";
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
// Commands////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////


export function addCommands(this: TimestampPlugin){    
    ///////////////////////////////////////////////////////////////////////////////////////////////
    //Recorder//////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////		
    this.addCommand({
        id: 'recorder-plus-open',
        name: 'Open Recorder+',//Prompt Modal
        callback: () => {
            utils.activateRecorderView(this);
        }
    });

    this.addCommand({
        id: 'recorder-plus-toggle-pause',
        name: 'pause /Resume Recorder+ ',//Prompt Modal
        callback: () => {
            utils.activateRecorderView(this);
        }
    })
    
    this.addCommand({
        id: 'recorder-plus-stop',
        name: 'Stop Recorder+',//Prompt Modal
        callback: () => {
            utils.activateRecorderView(this);
        }
    })
}